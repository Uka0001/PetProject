package ShildtBookTasks;

public class SymbolsJava {
    public static void main(String[] args) {
        System.out.println("\' Одинарная кавычка");
        System.out.println("\" Двойная кавычка");
        System.out.println("\\ Обратная косая черта");
        System.out.println("\r Возврат каретки\n");
        System.out.println("\n Перевод строки");
        System.out.println("\f Перевод страницы");
        System.out.println("\t Горизонтальная табуляция");
        System.out.println("\b Возврат на одну позицию");
        System.out.println("\012 Восьмеричная константа (восьмеричное число)");
        System.out.println("\u1234 Шестнадцатеричная константа (шестнадцатеричное число)");
    }
}
